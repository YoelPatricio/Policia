    public void cargarDatos() {
        
        DenunciaDAO dao = new DenunciaDAO();
        
        List<Object> datos = dao.getDenunciasList(txtBuscar.getText());

        Object[][] matriz = new Object[datos.size()][12];
        Object[] s = new Object[]{};
        for (int i = 0; i < datos.size(); i++) {
            
            s=(Object[]) datos.get(i);
            //System.out.println(s[0]);
            
            matriz[i][0] = s[0];
            matriz[i][1] = s[1];
            matriz[i][2] = s[2];
            matriz[i][3] = s[3];
            matriz[i][4] = s[4];
            matriz[i][5] = s[5];
            matriz[i][6] = s[6];
            matriz[i][7] = s[7];
            matriz[i][8] = s[8];
            matriz[i][9] = s[9];
            matriz[i][10] = s[10];
            matriz[i][11] = s[11];

        }
        Object[][] data = matriz;
        String[] cabecera = {"C贸digo", "Delito","Tipo Delito", "SubTipo Delito","SubDetalle Delito", "Modalidad", "Fecha Registro","Hora Registro","Fecha Hecho","Hora Hecho","Afectado","Direcci贸n"};
        dtm = new DefaultTableModel(data, cabecera);
        tableDatos.setModel(dtm);
    }
