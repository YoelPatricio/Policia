    public void cargarDatos() {
        
        if(personaDenunciaList==null){
            return;
        }
        
        
        
        List<PersonaDenuncia> datos = personaDenunciaList;

        Object[][] matriz = new Object[datos.size()][2];
        
        for (int i = 0; i < datos.size(); i++) {
            
            
            //System.out.println(s[0]);
            
            matriz[i][0] = datos.get(i).getPersona().getDni();
            matriz[i][1] = datos.get(i).getPersona().getApelNomb();
 
        }
        Object[][] data = matriz;
        String[] cabecera = {"DNI","Apellidos y Nombres"};
        dtm = new DefaultTableModel(data, cabecera);
        tableDatos.setModel(dtm);
    }
