    private void cargarDatos() {
        DenunciaDAO dd = new DenunciaDAO();
        
        List<Object> datos = dd.getPersonasEnDenuncia(txtBuscar.getText());

        Object[][] matriz = new Object[datos.size()][5];
        Object[] s = new Object[]{};
        for (int i = 0; i < datos.size(); i++) {
            
            s=(Object[]) datos.get(i);
            //System.out.println(s[0]);
            
            matriz[i][0] = s[0];
            matriz[i][1] = s[1];
            matriz[i][2] = s[2];
            matriz[i][3] = s[3];
            matriz[i][4] = s[4];
            

        }
        txtTotalDenuncias.setText(String.valueOf(datos.size()));
        Object[][] data = matriz;
        String[] cabecera = {"C√≥digo", "DNI","Apellidos y Nombres", "Situacion","Estado"};
        dtm = new DefaultTableModel(data, cabecera);
        tableDatos.setModel(dtm);    }
        

