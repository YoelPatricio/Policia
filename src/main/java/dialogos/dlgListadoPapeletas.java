    private void cargarDatos() {
        PapeletaDAO dao = new PapeletaDAO();
        
        List<Object> datos = dao.getTiposPapeletas(txtBuscar.getText());

        Object[][] matriz = new Object[datos.size()][4];
        Object[] s = new Object[]{};
        for (int i = 0; i < datos.size(); i++) {
            
            s=(Object[]) datos.get(i);
            //System.out.println(s[0]);
            
            matriz[i][0] = s[0];
            matriz[i][1] = s[1];
            matriz[i][2] = s[2];
            matriz[i][3] = s[3];

        }
        Object[][] data = matriz;
        String[] cabecera = {"C칩digo", "Gravedad","Descripcion", "Monto"};
        dtm = new DefaultTableModel(data, cabecera);
        tableDatos.setModel(dtm);

    }
    
